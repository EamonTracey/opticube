const uint32_t corner_o_radix[] = { 0, 729, 243, 81, 27, 9, 3, 1 };
const uint32_t corner_p_radix[] = { 5040, 720, 120, 24, 6, 2, 1, 1 };

uint32_t corners_state(struct cube *cube) {
    uint8_t cubelet = cube->corners[0].cubelet;
    uint8_t visited = 128 >> cubelet;

    uint32_t orientation = 0;
    uint32_t permutation = cubelet * corner_p_radix[0];

    for (int i = 1; i < 8; ++i) {
        uint8_t cubelet = cube->corners[i].cubelet;
        visited |= 128 >> cubelet;

        orientation += cube->corners[i].orientation * corner_o_radix[i];
        permutation += (cubelet - bit_count[(uint8_t)(visited >> (8 - cubelet))]) * corner_p_radix[i];
    }

    return orientation + permutation * 2187;
}
